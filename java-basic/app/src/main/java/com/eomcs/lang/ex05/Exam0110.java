package com.eomcs.lang.ex05;

//# 산술 연산자 : +, -, *, /, %
//
public class Exam0110 {
  public static void main(String[] args) {
    System.out.println(100 + 27); //127
    System.out.println(100 - 27); //73
    System.out.println(100 * 27); // 2700
    System.out.println(100 / 27); // 3 나눈 몫
    System.out.println(100 % 27); // 19 나눈 나머지 값
  }
}